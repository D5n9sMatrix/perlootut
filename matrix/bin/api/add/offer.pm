sub IR($){
    my $self    = shift;
    my $reals   = $self->{"cpi"} if $self.value($_, 400); # search value rules
    my $summary = $self->{"tab"} if $self.value($_, 400); # effect value number
    my $cpi     = $self->{"cov"} if $self.value($_, 400); # called value number

    if ($self eq $reals gt $summary){
        return $cpi;
    }
}

